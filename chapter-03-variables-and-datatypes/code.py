# Integer (int)
steps_walked = 8000
print(steps_walked)

# Float (float)
weight_kg = 72.5
print(weight_kg)

# String (str)
user_name = "JP"
print(user_name)

# Boolean (bool)
goal_reached = True
print(goal_reached)

# Custom Print with Context
print("Hello,", user_name)
print("Goal reached today?", goal_reached)

# Comment Example
# This is a comment. Python will ignore this line.
name = "JP"  # You can also write comments after code

# String vs Float Addition
a = 5
b = 3
print("Sum of numbers:", a + b)

first_name = "Jonathan"
last_name = "Perrera"
print("Full name:", first_name + last_name)
print("Full name:", first_name + " " + last_name)

# Strings that look like numbers
a = "10"
b = "5"
print("Combined as strings:", a + b)

